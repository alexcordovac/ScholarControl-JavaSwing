    //Método que oculta un jlabel
    public void ocultar(JLabel label, int tiempo){
        ActionListener tarea = (ActionEvent evt) -> {
            label.setText("");
        };
     Timer timer = new Timer(tiempo, tarea);
     timer.setRepeats(false);
     timer.start();
    }
    
    //Advertencia inicial en caso de que no se haya ingresado el total de alumnos
    public void advertencia(){
        labelResultado.setForeground(Color.red);
        labelResultado.setText("¡Ingresa el total de alumnos!");
        this.ocultar(labelResultado, 1000);
    }
    
    
    //Verificar los caracteres introducidos en un campo
    public void verificarCaracteres(java.awt.event.KeyEvent evt, JLabel etiqueta){
    char validar = evt.getKeyChar();
   
        if(Character.isLetter(validar)){
            getToolkit().beep();
            evt.consume();
            
            etiqueta.setForeground(Color.blue);
            etiqueta.setText("Ingrese sólo números enteros");
            this.ocultar(etiqueta, 1000);
        }
    }
    
