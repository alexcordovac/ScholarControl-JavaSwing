    //Al presionar enter en el campo total de alumnos
    private void jTextField2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyPressed
        if(evt.getKeyChar()==KeyEvent.VK_ENTER){
            try{
                int temp = Integer.parseInt(jTextField2.getText());
                if(temp>=1){
                    totalAlumnos = temp;
                    servicio.calificaciones = new int[1][temp];
                    jTextField2.setEnabled(false);
                    labelResultado.setForeground(new Color(0,153,0));
                    labelResultado.setText("¡Agregado!");
                    this.ocultar(labelResultado, 1000);
                }else{
                    labelResultado.setForeground(Color.red);
                    labelResultado.setText("¡No valido!");
                    this.ocultar(labelResultado, 1000);
                }
            }catch(Exception e){
                labelResultado.setForeground(Color.red);
                labelResultado.setText("¡Error!");
                this.ocultar(labelResultado, 1000);
            }
        }
    }//GEN-LAST:event_jTextField2KeyPressed

