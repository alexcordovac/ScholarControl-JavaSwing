    //Función para resetar el programa
    private void jPanel8MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel8MousePressed
        try{
            servicio.asignaturas = new String[1][2];
            totalAlumnos = 0;
            jTextField2.setEnabled(true);
            jTextField2.setText("");
            jTextField2.requestFocus();
            labelResultado.setForeground(new Color(0,153,0));
            labelResultado.setText("¡Reseteo correcto!");
            this.ocultar(labelResultado, 1000);
            }catch(Exception e){
                labelResultado.setForeground(Color.red);
                labelResultado.setText("¡Error al resetear! :/");
                this.ocultar(labelResultado, 1000);
            }
    }//GEN-LAST:event_jPanel8MousePressed

